mylist=[10,20,30,40,50,60,70]
print(mylist)
#add
mylist.append(40)
mylist.append(90)
print(mylist)
#insert
mylist.insert(1,80)
print(mylist)
#extends
mylist.extend([1,2,3])
print(mylist)
#remove
print(mylist)
mylist.remove(40)
print(mylist)
#pop by index
mylist.pop(0)
print(mylist)
#index
x=mylist.index(90)
print(x)
#count
x=mylist.count(10)
print(x)
#clear
mylist.clear()
print(mylist)

